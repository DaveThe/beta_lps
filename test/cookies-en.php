<div class="subtitle-cookie">HOW TO DEACTIVATE COOKIES</div>
<div class="descr-all-cookie number">
Most browsers (Internet Explorer, Firefox, Chrome, etc.) are configured to accept cookies. The cookies memorized on the hard disk of a device may nonetheless be erased and cookies can also be disenabled following the instructions given by major browsers at the following links:<br /><br />
<strong>Chrome</strong>
<ul>
<li>Click on the Chrome menu in the browser tool bar.</li>
<li>Select Settings.</li>
<li>Click on Show Advanced Settings.</li>
<li>In the Privacy section click on Content Settings.</li>
<li>In the Cookies section, modify cookie settings</li>
</ul>
<br /><br />

<strong>Safari</strong><br />
To block cookies on Safari iOS 8, tap Settings > Safari > Block Cookies and choose "Always Allow", "Allow from Websites I Visit", "Allow from Current Web Site", or "Always Block".  In iOS 7 or earlier, choose "Never", "From third parties and advertisers", or "Always". 
<br /><br />

<strong>Firefox</strong>
<ul>
<li>Click on the menu button and select Preferences.</li>
<li>Select the Privacy panel.</li>
<li>From Timeline Settings select Custom Settings. </li>
<li>To activate cookies, check Accept Cookies from Sites; to deactivate, uncheck Accept Cookies from Sites.</li>
</ul>
<br /><br />


<strong>Explorer</strong><br />
<ul>
<li>To open Internet Explorer, click on the Start button. Type Internet Explorer in the search window and then click on Internet Explorer in the results list.</li>
<li>Click on the Tools button and then on Internet Options.</li>
<li>Click on the Privacy tab and in Settings swipe upwards to block all cookies, or downwards to allow all cookies, then click OK.</li>
</ul>
