<div class="subtitle-cookie">WHAT ARE COOKIES?</div>
<div class="descr-all-cookie">
Cookies are short text files containing information about user browsing which websites memorize during navigation in order to improve it.<br /><br />

While a user browses a website it sends cookies to the device being used (PC, notebook, tablet, smartphone). As a rule the cookies will be stored on the user’s browser.<br />
The website that sent the cookies will then be able to read and record the others stored on the device, thus acquiring various types of information about the user.<br />
What kind of information? Each type of cookie has a very precise role.

</div>

<div class="subtitle-cookie">WHAT KIND OF COOKIES EXIST?</div>
<div class="descr-all-cookie">
There are three large categories of cookies, each with its own characteristics:<strong> technical cookies, profiling cookies and third-party cookies</strong>.<br /><br />

<strong>1. TECHNICAL COOKIES</strong><br />
These are necessary for the proper functioning of the website and are essential for navigation. Without technical cookies a user may not be able to display pages properly, or be able to use certain services.
A technical cookie, for example, is crucial for keeping users connected during their entire visit to a website or for saving language and display settings, etc.
<br /><br />

Technical cookies can then be further classified as: <strong>navigation cookies, analytics cookies and feature cookies</strong>:<br /><br />

<strong>navigation cookies</strong> are required for normal navigation and use of the website, (for example to make a purchase or for authentication to access restricted areas);<br />
<strong>analytic cookies</strong> are assimilated by technical cookies only when used by the website operator to collect aggregate information on the number of users and how they visit the site;<br />
<strong>feature cookies</strong> allow the user to browse according to a number of selected criteria (for example, language, products selected for purchase, etc.) so as to enhance the browsing experience.
<br /><br />

<strong>2. PROFILING COOKIES</strong><br />
Used to create a user profile and then send online advertising messages in line with the preferences expressed by the users when they are navigating.<br /><br />

<strong>3. THIRD-PARTY COOKIES</strong><br />
Generated and maintained by parties other than the administrator of the website the user is browsing (usually governed by a contract between the website owner and the third party).<br /><br />



All cookies may also be classified as follows:<br />
<strong>session cookies</strong> that are erased when the browser is closed;<br />
<strong>persistent cookies</strong> that remain in the browser for a specific length of time.<br />
For instance, the latter are used to recognize the device connecting to the site and make it easier for the user to perform authentication operations.

</div>

