<div class="subtitle-cookie">CHE COSA SONO I COOKIE?</div>
<div class="descr-all-cookie">
I cookie sono brevi file di testo contenenti i dati della tua navigazione che i siti web memorizzano, durante la navigazione stessa, al fine di migliorarla.<br /><br />

Mentre navighi all&apos;interno di un sito web, quest&apos;ultimo invia al dispositivo che stai utilizzando (pc, notebook, tablet, smartphone) i cookie. Questi, di norma, sono conservati sul browser col quale navighi.<br />
Il sito web che ha trasmesso i cookie, poi, pu&ograve; leggere e registrare quelli che si trovano sullo stesso dispositivo, al fine di ottenere informazioni di vario genere, in merito all&apos;utente.<br />
Quali informazioni? Ogni tipo di cookie ha un ruolo ben definito.
</div>

<div class="subtitle-cookie">QUALI TIPI DI COOKIE ESISTONO?</div>
<div class="descr-all-cookie">
Esistono tre grandi categorie di cookie, ognuna con proprie caratteristiche: <strong>cookie tecnici, cookie di profilazione</strong> e <strong>cookie di terze parti</strong>.<br /><br />

<strong>1. COOKIE TECNICI</strong><br />
Sono necessari per il corretto funzionamento del sito web e indispensabili per consentire la navigazione. In assenza di cookie tecnici potresti non visualizzare correttamente le pagine, oppure potresti non essere in grado di utilizzare alcuni servizi.<br />
Un cookie tecnico, ad esempio, &egrave; fondamentale per mantenere l&apos;utente collegato durante l&apos;intera visita a un sito web, oppure per memorizzare le impostazioni di lingua, visualizzazione, ecc...<br /><br />

I cookie tecnici sono ulteriormente distinguibili in: <strong>cookie di navigazione</strong>, <strong>cookie analytics</strong> e <strong>cookie di funzionalit&agrave;</strong>.<br /><br />

I <strong>cookie di navigazione</strong> garantiscono la regolare navigazione e fruizione del sito web (ad esempio, consentono di effettuare un acquisto, oppure di procedere all&apos;autenticazione per accedere alle aree riservate);<br />
I <strong>cookie analytics</strong> sono assimilati ai cookie tecnici solo quando utilizzati direttamente dal gestore del sito per raccogliere informazioni, in forma aggregata, sul numero di utenti e sulle modalit&agrave; con le quali questi ultimi visitano il sito stesso;<br />
I <strong>cookie di funzionalit&agrave;</strong> permettono all&apos;utente di navigare secondo una serie di criteri selezionati (ad esempio, lingua, prodotti selezionati per l&apos;acquisto, ecc..), al fine di migliorare il servizio reso all&apos;utente stesso.<br /><br />

<strong>2. COOKIE DI PROFILAZIONE</strong><br />
Hanno il compito di profilare l&apos;utente. Sono utilizzati al fine di inviare messaggi pubblicitari in linea con le preferenze manifestate dall&apos;utente stesso, durante la sua navigazione.<br /><br />

<strong>3. COOKIE DI TERZA PARTE</strong><br />
Sono generati e gestiti da soggetti diversi rispetto all&apos;amministratore del sito web sul quale l&apos;utente sta navigando (generalmente, in forza di un contratto tra il titolare del sito e la terza parte).<br /><br />



Tutti i cookie possono essere catalogati altres&igrave; secondo la categorizzazione di seguito:<br />
i <strong>cookie di sessione</strong> vengono cancellati alla chiusura del browser di navigazione;<br />
i <strong>cookie persistenti</strong> restano all&apos;interno del browser per un determinato periodo di tempo.<br />
Questi ultimi sono utilizzati, ad esempio, per riconoscere il dispositivo che si collega al sito, facilitando le operazioni di autenticazione da parte dell&apos;utente.
</div>

